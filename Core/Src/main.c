void I2C_Scan(void)
{
  for(uint8_t addr = 1; addr < 128; addr++)
  {
    if(HAL_I2C_IsDeviceReady(
            &hi2c1,
            addr << 1,
            2,
            10) == HAL_OK)
    {
      printf("Found:0x%02X\r\n", addr);
    }
  }
}
